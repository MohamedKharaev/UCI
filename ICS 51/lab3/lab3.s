	mul $t2, $t2, $t6		# M[1][1] * y

	add $t1, $t1, $t2		# M[1][0]x + M[1][1]y
	add $t1, $t1, $t3		# M[1][0]x + M[1][1]y + M[1][2] ... Represents y0

	blt $t1, $zero, SetZero
	blt $t4, $zero, SetZero
	addi $t2, $a3, -1
	bgt $t1, $t2, SetZero
	bgt $t4, $t2, SetZero		# Check if x0 and y0 are in bounds of the picture

	mul $t3, $a3, $t1
	add $t3, $t3, $t4		# Get pixel location for (x0, y0) in 1D array

