############################## Part 1: your code begins here ###
mul $t5, $a2, $a2			# size of 1d array
li $t0, 0				# set counter to 0 for loop
li $t2, 0xFF				# black
li $t3, 0x00				# white
WhileLoop:
	lbu $t1, 0($a0)			# load first byte from input buffer to $t1
	bgt $t1, $a3, OutputBlack	# if $t1 greater than $a3, jump to OutputBlack
	blt $t1, $a3, OutputWhite	# if $t1 less than $a3, jump to OutputWhite

OutputWhite:
	sb $t3, ($a1)			# store white value into current byte address in $a1
	j BottomOfLoop			# jump to BottomOfLoop to skip OutputBlack
OutputBlack:
	sb $t2, ($a1)			# store black value into current byte address in $a1

BottomOfLoop:
	addi $a0, $a0, 1		# move input buffer address over by 1
	addi $a1, $a1, 1		# move output buffer address over by 1
	addi $t0, $t0, 1		# add to counter by 1
	blt $t0, $t5, WhileLoop		# if counter less than 1d array size, go back up to top of loop
